# Pobieranie długości boków prostokąta od użytkownika
x = float(input("Podaj długość pierwszego boku prostokąta: "))
y = float(input("Podaj długość drugiego boku prostokąta: "))

# Obliczanie pola prostokąta
pole = x * y

# Obliczanie obwodu prostokąta
obwod = 2 * (x + y)

# Wyświetlanie wyników
print("Pole prostokąta wynosi:", pole)
print("Obwód prostokąta wynosi:", obwod)